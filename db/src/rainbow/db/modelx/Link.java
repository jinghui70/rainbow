package rainbow.db.modelx;

public class Link {

	private LinkPart left;
	
	private LinkPart right;

	public LinkPart getLeft() {
		return left;
	}

	public void setLeft(LinkPart left) {
		this.left = left;
	}

	public LinkPart getRight() {
		return right;
	}

	public void setRight(LinkPart right) {
		this.right = right;
	}
	
}
