package rainbow.db.dao.object;

public class _Report {
	
	private int count;
	
	private _Unit unit;
	
	private double money;

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public _Unit getUnit() {
		return unit;
	}

	public void setUnit(_Unit unit) {
		this.unit = unit;
	}

	public double getMoney() {
		return money;
	}

	public void setMoney(double money) {
		this.money = money;
	}

}
